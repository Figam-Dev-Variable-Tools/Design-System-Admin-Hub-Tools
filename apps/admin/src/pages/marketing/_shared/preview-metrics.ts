// 채널 미리보기의 치수 정본 (마케팅 발송 화면 공용 — pages/marketing/**)
//
// [왜 한 곳에 모으나] 미리보기 목업은 네 곳(발송 SMS·발송 이메일·뉴스레터·템플릿)에 흩어져 있고,
// 각자 `calc(var(--tds-space-6) * 9)` 같은 배수를 손으로 적어 왔다. 그 결과 같은 '휴대폰' 이
// 화면마다 다른 폭으로 그려졌고, 이메일 목업은 아예 상한이 없어 미리보기 열 전체(700px+)로
// 늘어났다 — 실제 수신 화면과 닮지 않은, 그냥 큰 빈 판이 됐다.
//
// [숫자의 근거] 목업의 목적은 '수신자가 보게 될 폭' 을 재현하는 것이므로 매체의 실제 폭을 쓴다.
// 토큰에 없는 파생 치수는 space 토큰의 calc 배수로만 표현한다 (G6 규칙).

/**
 * 휴대폰 화면 폭 — 360px (space.6 × 15).
 *
 * 실측 기준 세로 모드 안드로이드/아이폰의 CSS 폭이 360~390px 대다. 종전 216px 는 실제보다 한참
 * 좁아, 90byte 가 몇 줄로 접히는지(SMS→LMS 승격 판단의 핵심)를 과장해서 보여 줬다.
 */
export const PHONE_WIDTH = 'calc(var(--tds-space-6) * 15)';

/**
 * 메일 본문 폭 — 600px (space.6 × 25).
 *
 * 이메일 템플릿의 사실상 표준 폭이다(대부분의 클라이언트가 이 폭을 기준으로 레이아웃한다).
 * 상한이 없으면 미리보기 열을 그대로 채워, 실제보다 훨씬 넓은 줄바꿈을 보여 준다.
 */
export const MAIL_WIDTH = 'calc(var(--tds-space-6) * 25)';

/**
 * 목업 본문의 최소 높이 — 72px (space.6 × 3).
 *
 * 비었을 때 프레임이 납작해지지 않을 만큼만. 이보다 키우면 '큰 빈 판' 문제가 돌아온다.
 */
export const MOCK_BODY_MIN_HEIGHT = 'calc(var(--tds-space-6) * 3)';
